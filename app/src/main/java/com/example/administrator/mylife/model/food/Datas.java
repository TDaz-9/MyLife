package com.example.administrator.mylife.model.food;

/**
 * 第二层data数据
 */

public class Datas {
    private int id;
    private String title;
    private String tags;
    private String imtros;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTags() {
        return tags;
    }

    public void setTags(String tags) {
        this.tags = tags;
    }

    public String getImtros() {
        return imtros;
    }

    public void setImtros(String imtros) {
        this.imtros = imtros;
    }
}
